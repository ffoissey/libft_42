#include "ft_memory.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ptr_tmp;

	ptr_tmp = (unsigned char *)s;
	while (n-- > 0)
		*ptr_tmp++ = '\0';
}
