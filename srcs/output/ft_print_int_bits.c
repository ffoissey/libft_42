#include "ft_printf.h"

void	ft_print_int_bits(int m)
{
	int		i;

	i = 31;
	while (i >= 0)
	{
		if (m >> i & 1)
			write(1, "1", 1);
		else
			write(1, "0", 1);
		if (!(i % 8) && i != 0)
			write(1, " ", 1);
		i--;
	}
	write(1, "\n", 1);
}
